function y = EW09_obs(x)

n_col = size(x,2);

hc=0.5;
a=((0.2+x(2,:))/2+tanh(x(1,:)/(9.5*hc)).*(0.2-x(2,:))/2);
Ci=1-((0.8-(.5*x(2,:)+.5*a))./0.6);
y0=-75;
Cp=(0.5*tanh(0.1*(x(1,:)-y0))+0.5).*((1-(0.5*(0.2./a).^1000+0.5*(0.2./x(2,:)))).*(1-x(2,:)./0.8));

y = zeros(5,n_col);

y(1,:)=abs(x(1,:).*x(2,:));
y(2,:)=x(2,:)-a;
y(3,:)=(a).*abs(x(1,:));
y(4,:)=(.5+.4.*tanh((-(x(1,:)-50)/10))).*((x(1,:))+273.15);
y(5,:)=Cp.*Ci;

end

